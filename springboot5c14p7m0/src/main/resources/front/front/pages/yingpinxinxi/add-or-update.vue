<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				qiyemingcheng: '',
				gangweimingcheng: '',
				tupian: '',
				jianlitoudi: '',
				yingpinshuoming: '',
				toudishijian: '',
				xuehao: '',
				xingming: '',
				zhuanye: '',
				xueli: '',
				shouji: '',
				shhf: '',
				},
				// 登录用户信息
				user: {},
				ro:{
				   qiyemingcheng : false,
				   gangweimingcheng : false,
				   tupian : false,
				   jianlitoudi : false,
				   yingpinshuoming : false,
				   toudishijian : false,
				   xuehao : false,
				   xingming : false,
				   zhuanye : false,
				   xueli : false,
				   shouji : false,
				   sfsh : false,
				   shhf : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.toudishijian =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.xuehao = this.user.xuehao
			this.ro.xuehao = true;
			this.ruleForm.xingming = this.user.xingming
			this.ro.xingming = true;
			this.ruleForm.shouji = this.user.shouji
			this.ro.shouji = true;

			this.ro.qiyemingcheng = true;
			this.ro.gangweimingcheng = true;
			this.ro.toudishijian = true;
			this.ro.xuehao = true;
			this.ro.xingming = true;
			this.ro.shouji = true;


			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yingpinxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='qiyemingcheng'){
						this.ruleForm.qiyemingcheng = obj[o];
						this.ro.qiyemingcheng = true;
						continue;
					}
					if(o=='gangweimingcheng'){
						this.ruleForm.gangweimingcheng = obj[o];
						this.ro.gangweimingcheng = true;
						continue;
					}
					if(o=='tupian'){
						this.ruleForm.tupian = obj[o].split(",")[0];
						this.ro.tupian = true;
						continue;
					}
					if(o=='jianlitoudi'){
						this.ruleForm.jianlitoudi = obj[o];
						this.ro.jianlitoudi = true;
						continue;
					}
					if(o=='yingpinshuoming'){
						this.ruleForm.yingpinshuoming = obj[o];
						this.ro.yingpinshuoming = true;
						continue;
					}
					if(o=='toudishijian'){
						this.ruleForm.toudishijian = obj[o];
						this.ro.toudishijian = true;
						continue;
					}
					if(o=='xuehao'){
						this.ruleForm.xuehao = obj[o];
						this.ro.xuehao = true;
						continue;
					}
					if(o=='xingming'){
						this.ruleForm.xingming = obj[o];
						this.ro.xingming = true;
						continue;
					}
					if(o=='zhuanye'){
						this.ruleForm.zhuanye = obj[o];
						this.ro.zhuanye = true;
						continue;
					}
					if(o=='xueli'){
						this.ruleForm.xueli = obj[o];
						this.ro.xueli = true;
						continue;
					}
					if(o=='shouji'){
						this.ruleForm.shouji = obj[o];
						this.ro.shouji = true;
						continue;
					}
				}
			}
			this.styleChange()
			this.$forceUpdate()
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数


			// 日长控件选择日期时间
			toudishijianConfirm(val) {
				console.log(val)
				this.ruleForm.toudishijian = val.result;
				this.$forceUpdate();
			},


			tupianTap() {
				let _this = this;
				if(this.ro.tupian){
					return false
				}
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
			jianlitoudiTap() {
				let _this = this;
				if(this.ro.jianlitoudi){
					return false
				}
				this.$api.upload(function(res) {
					_this.ruleForm.jianlitoudi = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				let that = this
				//跨表计算判断
				var obj;
				if((!this.ruleForm.jianlitoudi)){
					this.$utils.msg(`简历投递不能为空`);
					return
				}
				if((!this.ruleForm.yingpinshuoming)){
					this.$utils.msg(`应聘说明不能为空`);
					return
				}
				if((!this.ruleForm.xuehao)){
					this.$utils.msg(`学号不能为空`);
					return
				}
				if((!this.ruleForm.xingming)){
					this.$utils.msg(`姓名不能为空`);
					return
				}
				if((!this.ruleForm.zhuanye)){
					this.$utils.msg(`专业不能为空`);
					return
				}
				if((!this.ruleForm.xueli)){
					this.$utils.msg(`学历不能为空`);
					return
				}
				if(this.ruleForm.shouji&&(!this.$validate.isMobile(this.ruleForm.shouji))){
					this.$utils.msg(`手机应输入手机格式`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						if(!obj) {
							obj = uni.getStorageSync('crossObj');
						}
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
							   crossuserid=Number(uni.getStorageSync('appUserid'));
							   crossrefid=obj['id'];
							   crossoptnum=uni.getStorageSync('statusColumnName');
							   crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`yingpinxinxi`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						uni.removeStorageSync('crossCleanType');
						return false;
					} else {
				//跨表计算
						let oet = {}
						if(this.ruleForm.id){
							await this.$api.update(`yingpinxinxi`, this.ruleForm);
						}else{
							oet = await this.$api.add(`yingpinxinxi`, this.ruleForm);
						}
						if(this.cross){
							uni.setStorageSync('crossCleanType',true);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
				//跨表计算
					let oet = {}
					if(this.ruleForm.id){
						await this.$api.update(`yingpinxinxi`, this.ruleForm);
					}else{
						oet = await this.$api.add(`yingpinxinxi`, this.ruleForm);
					}
					if(this.cross){
						uni.setStorageSync('crossCleanType',true);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
